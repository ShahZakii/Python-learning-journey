# f-strings in python.

# It is a new string formatting mechanism introduced by the PEP 498. It is also known as Literal String Interpolation or more commonly 
# as F-strings(f character preceding the string literal).The primary focus of this mechanism is to make the interpolation easier.

# When we prefix the string with the letter "f", the string becomes the f-string itself. The f-string can be formatted in much same as the
# str.format() method. The f-string offers a convential way to embed Python expression inside literals for formatting.

name = "Zia ul haq"
age = 15
print(f"\nHello! My name is {name} and i'm {age} years old.\n")