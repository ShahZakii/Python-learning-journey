# 1. Variables.

name = "Zaki Shah"
age = 22
height = 5.11
is_studet = True

print("Name: ",name)
print("Age: ",age)
print("Height: ",height)
print("Student: ",is_studet)


# 2. Checking data types.

print(type(name))                    # string
print(type(age))                     # integer
print(type(height))                  # float
print(type(is_studet))               # boolean


# 3. Taking user input.

name = input("Enter your name: ")
print("Nice to meet you: ",name)


# 4. Basic operations.

a = 13
b = 3

print("Addition: ",a+b)                # 16                 
print("Subtraction: ",a-b)             # 10
print("Multiplication: ",a*b)          # 39
print("Division: ",a/b)                # 4.33
print("Remainder: ",a%b)               # 1
print("Exponentiation: ",a**b)         # 2197

